   /**
 * This is a simple Java program to demonstrate different types of comments.
 */
class code3 {

    public static void main(String[] args) {
        // This is a single-line comment. It explains the next line of code.
        System.out.println("Hello, World!"); // Print greeting message

        /*
         * This is a multi-line comment.
         * It can span multiple lines and is useful for providing
         * detailed explanations or notes about the code.
         */
        System.out.println("Java programming!"); // Print welcome message
    }
}

