public class Operators {
    public static void main(String[] args) {
        int a = 5, b = 10;
        System.out.println("a: " + a + ", b: " + b);
        System.out.println("Equal Operator (a == b): " + (a == b));
        System.out.println("Not Equal Operator (a != b): " + (a != b));
    }
}
